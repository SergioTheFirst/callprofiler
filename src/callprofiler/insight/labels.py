def describe_dim(name, z, thr=1.0):
    """Фраза для оси по знаку/величине z. None если ниже порога / нет фразы."""
    entry = FEATURE_LABELS.get(name)
    if not entry:
        return None
    _, high, low = entry
    if z >= thr and high:
        return high
    if z <= -thr and low:
        return low
    return None


def cluster_label(top_dims, thr=0.8, max_parts=2):
    """Детерминированное имя кластера из топ-осей (list[(name, z)] по |z| убыв)."""
    parts = []
    for name, z in top_dims:
        phrase = describe_dim(name, z, thr=thr)
        if phrase:
            parts.append(phrase)
        if len(parts) >= max_parts:
            break
    return " · ".join(parts) if parts else "смешанный профиль"
