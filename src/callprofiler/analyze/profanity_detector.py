# -*- coding: utf-8 -*-
"""
profanity_detector.py — словарный детектор русского мата.

Без LLM: regex-поиск по ~50 корням мата. Возвращает счётчик и плотность
матов на 100 слов. Результат используется enricher'ом как дополнительный
сигнал для bs_score / call_type — модель видит количественную метрику,
а не должна сама "слышать" мат в стенограмме.

Корни, а не точные слова: один корень "хуй" покрывает "хуй", "хуёвый",
"хуйня", "охуеть", "хуясе" и т.д. Это сознательный over-match (false
positives на словах вроде "схуяли" приемлемы — мат всё равно мат).
"""

from __future__ import annotations

import re
from typing import Iterable

# Словарь корней (~50). Только инфинитивные/корневые формы — regex
# подберёт все производные через \w*<root>\w*.
#
# Важно: не пытаемся поймать обфускации (х*й, х@й, x_y) — это работа
# отдельного слоя. Здесь — чистый словарь по кириллице.
_MAT_ROOTS: tuple[str, ...] = (
    # Большая четвёрка
    "хуй", "хуё", "хуе", "хуя",
    "пизд", "пезд",
    "ебa", "ёб", "еб", "ебл", "ебн", "ебу", "ебё", "ебе",
    "блят", "блядь", "бляд",
    # Производные / усилители
    "охуе", "охуи", "охуя",
    "пиздец", "пиздат", "пиздёж", "пиздёл", "пиздит", "пиздец",
    "ебан", "ебуч", "ебал", "ебись", "ёбн",
    "пидор", "пидар", "пидер",
    "мудак", "мудил", "муд",
    "залуп", "залупа",
    "сук", "сука", "сучар", "сучк",
    "гондон", "гандон",
    "гавно", "говно", "говн", "говнист", "говнюк",
    "срат", "сран", "сру", "срал", "сри",
    "жоп", "жоп",
    # Лёгкий мат / эвфемизмы (обычно считают)
    "хер", "херов", "херня",
    "блин",  # эвфемизм блять — спорно; оставим на низкой границе
    "нах", "нахуй", "нахер",
    "пох", "похуй", "похер",
    "охрен", "хрен",
    "трах", "тра́хн", "трахн",
    # Жаргон
    "лох", "лошар",
    "чмо",
    "шлюх", "шлюш",
)

# Соберём один скомпилированный regex: \b\w*(root1|root2|...)\w*\b
# \b — границы слов (кириллица в \w работает с re.UNICODE по умолчанию в Py3)
_PATTERN = re.compile(
    r"\b\w*(?:" + "|".join(re.escape(r) for r in _MAT_ROOTS) + r")\w*\b",
    re.IGNORECASE | re.UNICODE,
)

# Подсчёт слов: всё, что отделено пробелами/пунктуацией
_WORD_RE = re.compile(r"\w+", re.UNICODE)


def count_profanity(text: str | None) -> dict:
    """Подсчитать мат в тексте.

    Параметры:
        text — строка (стенограмма звонка). None и пустая строка → нули.

    Возвращает dict:
        {
          "count": int,    # всего вхождений мата
          "unique": int,   # уникальных матерных слов (по lowercase)
          "density": float # вхождений на 100 слов (0.0 если слов нет)
        }
    """
    if not text:
        return {"count": 0, "unique": 0, "density": 0.0}

    matches = _PATTERN.findall(text)
    count = len(matches)
    unique = len({m.lower() for m in matches})

    word_count = len(_WORD_RE.findall(text))
    density = (count / word_count * 100.0) if word_count > 0 else 0.0

    return {
        "count": count,
        "unique": unique,
        "density": round(density, 2),
    }


def find_profanity(text: str | None) -> list[str]:
    """Вернуть список найденных матерных слов (для отладки)."""
    if not text:
        return []
    return _PATTERN.findall(text)


def get_roots() -> Iterable[str]:
    """Вернуть текущий словарь корней (для тестов / документации)."""
    return _MAT_ROOTS
